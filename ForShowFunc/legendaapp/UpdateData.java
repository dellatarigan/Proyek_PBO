    public void findUsers()
    {
        ArrayList<Mahasiswa> users = ListUsers(jinsertnim.getText());
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"NIM","NAMA","PELANGGARAN","POIN","semester"});
        Object[] row = new Object[5];
        
        for(int i = 0; i < users.size(); i++)
        {
            row[0] = users.get(i).getID();
            row[1] = users.get(i).getNAMA();
            row[2] = users.get(i).getPELANGGARAN();
            row[3] = users.get(i).getPOIN();
            row[4] = users.get(i).getSEMESTER();
            model.addRow(row);
        }
       
    }
    
    

