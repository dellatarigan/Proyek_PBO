    public ArrayList<Mahasiswa> ListUsers(String ValToSearch)
    {
        ArrayList<Mahasiswa> usersList = new ArrayList<Mahasiswa>();
        
        Statement st;
        ResultSet rs;
        
        try{
            Connection con = getConnection();
            st = con.createStatement();
            String searchQuery;
            searchQuery = "SELECT * FROM mahasiswa where CONCAT(id_mahasiswa, nama, pelanggaran, poin_dimasukkan, semester_pemasukkan) LIKE '%"+ValToSearch+"%'";
            rs = st.executeQuery(searchQuery);
            
            Mahasiswa user1;
            
            while(rs.next())
            {
                user1 = new Mahasiswa(
                                 rs.getString("id_mahasiswa"),
                                 rs.getString("nama"),
                                 rs.getString("pelanggaran"),
                                 rs.getInt("poin_dimasukkan"),
                                 rs.getInt("semester_pemasukan"));
                
                usersList.add(user1);
            }
            
        }catch(SQLException ex){
            System.out.println(ex.getMessage());
        }
        
        return usersList;
    }
    
