    public ArrayList<User> ListUsers(String ValToSearch)
    {
        ArrayList<User> usersList = new ArrayList<User>();
        
        Statement st;
        ResultSet rs;
        
        try{
            Connection con = getConnection();
            st = con.createStatement();
            String searchQuery;
            searchQuery = "SELECT * FROM mahasiswa where CONCAT(id_mahasiswa, nama, jurusan, poin, gelar, semester) LIKE '%"+ValToSearch+"%'";
            rs = st.executeQuery(searchQuery);
            
            User user;
            
            while(rs.next())
            {
                user = new User(
                                 rs.getString("id_mahasiswa"),
                                 rs.getString("nama"),
                                 rs.getString("jurusan"),
                                 rs.getInt("poin"),
                                 rs.getString("gelar"),
                                 rs.getInt("semester"));
                
                usersList.add(user);
            }
            
        }catch(SQLException ex){
            System.out.println(ex.getMessage());
        }
        
        return usersList;
    }
