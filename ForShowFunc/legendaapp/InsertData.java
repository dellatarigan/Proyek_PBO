     public void findUsers()
    {
        ArrayList<User> users = ListUsers(jtextsearch.getText());
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"id_mahasiswa","nama","jurusan","poin","gelar","semester"});
        Object[] row = new Object[6];
        
        for(int i = 0; i < users.size(); i++)
        {
            row[0] = users.get(i).getNIM();
            row[1] = users.get(i).getNama();
            row[2] = users.get(i).getJurusan();
            row[3] = users.get(i).getPoin();
            row[4] = users.get(i).getGelar();
            row[5] = users.get(i).getSemester();
            model.addRow(row);
        }
       jtabelud.setModel(model);
       
    }
